# stacks in python


def stack_s(): # defining function
    myStack = [] # creating empty stack

    myStack.append('a') # inserting elements into stack
    myStack.append('b')
    myStack.append('c')
    print(myStack)


    myStack.pop() # deleting elements from stack
    print(myStack)

    myStack.pop()
    print(myStack)

    myStack.pop()
    print(myStack)
stack_s()  # function call





