# math function in python
import math # importing math module

x = math.sqrt(25) # using math performing square root of 25
print(x)

a = 9.56
print(math.floor(a)) # floor will give least value
print(math.ceil(a)) # ceil will give high value

print(math.pow(2,3)) # power function
print(math.pi) # pi value
print(math.e)

b=-980.787
