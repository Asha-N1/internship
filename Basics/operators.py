# some Operators in python

# some examples of Arithmetic operators
def main():
    n1 = 2
    n2 = 5
    print(n1%n2) # module operation
    print(n1**n2) # exponential
    print(n1//n2) # floor

    # some examples of Assignment operators
    n1 **= 3 # n1 = n1 ** 3
    print(n1)

    x = 5
    x *= 3 # x = x * 3
    print(x)

# main execution
if __name__ == '__main__':
    main()





