# math function in python
import math # importing math module

def squr_fun(): # squr function
    x = math.sqrt(25) # using math performing square root of 25
    print(x)
squr_fun() # calling function

def floor_fun(): #floor function
    a = 9.56
    print(math.floor(a)) # floor will give least value
    print(math.ceil(a)) # ceil will give high value
floor_fun() # calling function

def pow_fun():  # power function
    print(math.pow(2,3)) # power function
    print(math.pi) # pi value
    print(math.e)
pow_fun() # calling function
 
def positive():  # positive function
    b=-980.787
    print(abs(b)) # positive value
positive()   # calling function

def max_fun():
    l = max(12,3,5) # max build in math function
    print(l)
max_fun()

def min_fun():
    l = min(12,3,5) # min build in math function
    print(l)
min_fun()





